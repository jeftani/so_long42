#include "so_long.h"

void	gamestatus(int c)
{
	if (c == 'L')
	{
		write(1, "you lost", 8);
		exit(0);
	}
	if (c == 'G')
	{
		write(1, "GAME_OVER\n", 10);
		exit(0);
	}
}
